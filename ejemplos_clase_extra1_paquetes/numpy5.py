import numpy as np

valores = np.array([1, 2, 3, 4, 5])

# Media de los elementos
print("Media:", np.mean(valores))

# Mediana de los elementos
print("Mediana:", np.median(valores))

# Desviación estándar de los elementos
print("Desviación estándar:", np.std(valores))

# Varianza de los elementos
print("Varianza:", np.var(valores))

# Valor mínimo
print("Mínimo:", np.min(valores))

# Valor máximo
print("Máximo:", np.max(valores))

# Suma de los elementos
print("Suma:", np.sum(valores))

# Producto de los elementos
print("Producto:", np.prod(valores))