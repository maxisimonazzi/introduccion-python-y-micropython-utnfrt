from datetime import datetime

# Definir dos fechas
fecha1 = datetime(2024, 7, 9)
fecha2 = datetime(2024, 12, 8)

# Calcular la diferencia entre las dos fechas
print("-"*50)
print("Fecha 1:", fecha1)
print("Fecha 2:", fecha2)
print("-"*50)

diferencia = fecha2 - fecha1
print("Diferencia entre las dos fechas:", diferencia) # Imprime la diferencia entre las dos fechas