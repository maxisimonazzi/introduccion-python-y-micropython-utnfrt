import numpy as np

# Crear dos arrays
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

# Suma de arrays
suma = a + b
print("Suma:", suma)

# Producto de arrays
producto = a * b
print("Producto:", producto)

# Producto punto
producto_punto = np.dot(a, b)
print("Producto punto:", producto_punto)

# Funciones matemáticas
logaritmo = np.log(a)
print("Logaritmo:", logaritmo)

raiz_cuadrada = np.sqrt(a)
print("Raíz cuadrada:", raiz_cuadrada)